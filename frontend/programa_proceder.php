		<p class="mb-5">
			Son requisitos formales para la participación de un emprendimiento en el PROCEDER:
		</p>
		<p>
			* En el caso de las personas físicas, tener edad entre dieciocho (18) y cuarenta (40) años y contar con inscripción en AFIP
		</p>

		<p>
			* En el caso de las personas jurídicas, tener una antigüedad menor o igual de 7 (siete) años desde el momento de su inscripción en AFIP
		</p>

		<p>
			* Estar inscripto en ATER y contar con las habilitaciones municipales exigibles para el sector en que se desarrolla su actividad.
		</p>

		<p>
			* Estar radicado o a radicarse en la provincia de Entre Ríos.
		</p>

		<p>
			* Presentar un proyecto junto a un inversor/grupo inversor.
		</p>

		<p>
			<p>* Poseer al menos una de las siguientes condiciones: </p>
			<ul>
				<li>Ser una EBT (Empresa de Base Tecnológica).</li>
				<li>Ser un emprendimiento de triple impacto.</li>
				<li>Tener un marcado componente de innovación en la propuesta objeto del proyecto presentado. </li>
			</ul>
		</p>
	</div>
</div>



<div class="row mb-5">
	<div class="col-xs-12 col-sm-12 col-lg-12">
		<h5 style="color: #22bcef;">Inversores</h5>
		<p>
			<strong>Inversión Privada:</strong>
			Pueden ser personas físicas o jurídicas que estén interesadas en realizar aportes financieros a emprendimientos que cumplan con los requisitos antes mencionados. No será requisito que el inversor/grupo inversor esté radicado en la provincia de Entre Ríos. Deberá hacer un aporte financiero al emprendimiento por un monto de hasta <strong> $2.000.000 (PESOS dos millones)</strong>. Ser inversor de un emprendimiento no representará un obstáculo para invertir en otro.
		</p>
		<p>
			<strong>Inversióm Provincial:</strong>
			A través de la SDEyE, el Estado Provincial otorgará un aporte no reintegrable de hasta <strong> $2.000.000 (PESOS dos millones) </strong>, igualando el aporte realizado por el inversor privado. Este aporte público no implicará en ningún caso toma de porcentaje de capital del emprendimiento.
		</p>
	</div>
</div>

